    void limpiar(){
        txtid.setText("");
        txtnombre.setText("");
        txtapellido.setText("");
    
    }
    private void botguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botguardarActionPerformed
       try {
            int id = Integer.valueOf(txtid.getText());
            String nombre = txtnombre.getText();
            String apellidos = txtapellido.getText();
            boolean insertado = this.controlador.insertarPersona(id, nombre, apellidos);
            if (insertado == true) {
                JOptionPane.showMessageDialog(rootPane, "Persona guardada con exito");
                limpiar();
                tablapersona.setModel(this.controlador.personas());
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se pudo guardar");
            }
        } catch (NumberFormatException | HeadlessException e) {
            JOptionPane.showMessageDialog(rootPane, "El ID introducido no es un n√∫mero");
        }
    }//GEN-LAST:event_botguardarActionPerformed

    private void boteliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_boteliminarActionPerformed
       
        try {
            int id = Integer.parseInt(txtid.getText());
            boolean eliminar = this.controlador.eliminarPersona(id);
            if (eliminar == true) {
                JOptionPane.showMessageDialog(rootPane, "Persona eliminada");
                limpiar();
                tablapersona.setModel(this.controlador.personas());
            } else {
                JOptionPane.showMessageDialog(rootPane, "No se pudo eliminar");
            }
        } catch (NumberFormatException | HeadlessException e) {
            JOptionPane.showMessageDialog(rootPane, "No hay una persona seleccionada");
        }
    
    }//GEN-LAST:event_boteliminarActionPerformed

    private void botlimpiarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botlimpiarActionPerformed
        limpiar();
    }//GEN-LAST:event_botlimpiarActionPerformed

    private void botactualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botactualizarActionPerformed
         int id = Integer.valueOf(txtid.getText());
        String nombre = txtnombre.getText();
        String apellidos = txtapellido.getText();
        boolean insertado = this.controlador.actualizarPersona(id, nombre, apellidos);
        if (insertado == true) {
            JOptionPane.showMessageDialog(rootPane, "Persona Actualizada");
            limpiar();
            tablapersona.setModel(this.controlador.personas());
        } else {
            JOptionPane.showMessageDialog(rootPane, "No se pudo actualizar la persona");
        }
    }//GEN-LAST:event_botactualizarActionPerformed
    
    public void seleccionarPersonas() {
        tablapersona.addMouseListener(new MouseAdapter() {
            @Override
            public void mousePressed(MouseEvent Mouse_evt) {
                if (Mouse_evt.getClickCount() == 1) {
                    txtid.setText(tablapersona.getValueAt(tablapersona.getSelectedRow(), 0).toString());
                    txtnombre.setText(tablapersona.getValueAt(tablapersona.getSelectedRow(), 1).toString());
                    txtapellido.setText(tablapersona.getValueAt(tablapersona.getSelectedRow(), 2).toString());
                }
            }
        });
    }
